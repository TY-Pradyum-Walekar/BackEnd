package com.tyss.typecasting.data;

public class DataConversion {
	public static void main(String[] args) {
		
		
		float a= 1006.5f;
		int b = (int) a;
		
		double c= 1095.87;
		int d=(int)c;
		
		
		byte e=(byte)a;
		
		System.out.println(e);
		
		System.out.println(d);

		System.out.println(b);
	}

}
